func isBinary(r io.Reader) (bool, error) {
	buf := make([]byte, 1024)
	n, err := r.Read(buf)
	if err != nil && err != io.EOF {
		return false, err
	}

	for _, b := range buf[:n] {
		if isBinaryChar(b) {
			return true, nil
		}
	}

	return false, nil
}

func isBinaryChar(data byte) bool {
	return binaryFlags[data] == 1
}
